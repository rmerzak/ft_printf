#include "ft_printf_h.h"

void	ft_putnbhX(unsigned int i)
{
	char	str[16]="0123456789ABCDEF";

	if(i < 16)
	{
		write(1,&str[i],1);
	}
	else
	{
		ft_putnbhX(i / 16);
		ft_putnbhX(i % 16);
	}

}
