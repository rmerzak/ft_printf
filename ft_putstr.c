#include "ft_printf.h"

int ft_putstr(const char *str)
{
	size_t	i;

	i = 0;
	if (!str)
		str = "(null)";
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
			i++;
	}
	return (i);
}
